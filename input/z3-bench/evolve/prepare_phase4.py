"""
Materialize phase4_unified/initial_program.py with a literal dict containing
the union of phase 1/2/3 winners (loaded from shared/phase{1,2,3}_best.json).

Run AFTER phases 1, 2, 3 have completed (and extract_best.py ran for each).

This rewrites only the EVOLVE-BLOCK section of phase4_unified/initial_program.py.
"""
import json
import pathlib
import pprint
import re
import sys

ROOT = pathlib.Path(__file__).resolve().parent
SHARED = ROOT / "shared"
PHASE4_FILE = ROOT / "phase4_unified" / "initial_program.py"

_EVOLVE_BLOCK_RE = re.compile(
    r"(# EVOLVE-BLOCK-START\n).*?(# EVOLVE-BLOCK-END)",
    re.DOTALL,
)


def _load(name):
    f = SHARED / name
    if not f.exists():
        print(f"missing: {f}", file=sys.stderr)
        sys.exit(1)
    return json.loads(f.read_text())


def main():
    p1 = _load("phase1_best.json")
    p2 = _load("phase2_best.json")
    p3 = _load("phase3_best.json")
    merged = {}
    merged.update(p1)
    merged.update(p2)
    merged.update(p3)
    print(f"merged keys: p1={len(p1)} p2={len(p2)} p3={len(p3)} union={len(merged)}")

    src = PHASE4_FILE.read_text()
    dict_repr = pprint.pformat(merged, width=100, sort_dicts=True)
    new_block_body = (
        "# Auto-generated by prepare_phase4.py from union of phase 1/2/3 best.\n"
        f"UNIFIED_OVERRIDES = {dict_repr}\n"
    )
    new_src, n = _EVOLVE_BLOCK_RE.subn(
        lambda m: m.group(1) + new_block_body + m.group(2),
        src,
        count=1,
    )
    if n != 1:
        print(f"EVOLVE-BLOCK markers not found in {PHASE4_FILE}", file=sys.stderr)
        sys.exit(1)
    PHASE4_FILE.write_text(new_src)
    print(f"wrote {PHASE4_FILE.relative_to(ROOT)} ({len(merged)} keys in EVOLVE-BLOCK)")


if __name__ == "__main__":
    main()
